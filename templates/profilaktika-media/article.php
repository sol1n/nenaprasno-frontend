                Слова «профилактика» и «рак» редко можно встретить рядом. Обычно человек начинает узнавать, читать, говорить о раке только тогда, когда он сам сталкивается с этой болезнью. Пока же ее нет, человеку проще и приятнее не думать об этом заболевании, как о чем-то крамольном. А ведь рак может настигнуть каждого: это не то заболевание, которое приходит от промокших ног и которое легко предсказать. Поэтому важно рассказывать людям, что это за болезнь, почему она возникает, как ее выявлять и, что самое главное, как заниматься ее профилактикой. Ведь несмотря на то, что факторов возникновения рака очень много и нельзя угадать, что стало его причиной, каждый может снизить риск этого заболевания для себя и своих близких. Как сообщается на сайте Всемирной организации здравоохранения, одна треть всех заболеваний раком предотвратима.
            </p>

